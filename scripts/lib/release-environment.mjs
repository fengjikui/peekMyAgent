  "PEEKMYAGENT_TRANSLATION_BASE_URL",
  "PEEKMYAGENT_TRANSLATION_API_KEY",
  "PEEKMYAGENT_TRANSLATION_MODEL",
  "PEEKMYAGENT_TRANSLATION_CLAUDE_BIN",
  "ANTHROPIC_BASE_URL",
  "ANTHROPIC_AUTH_TOKEN",
  "ANTHROPIC_API_KEY",
  "ANTHROPIC_MODEL",
  "ANTHROPIC_SMALL_FAST_MODEL",
  "OPENAI_BASE_URL",
  "OPENAI_API_KEY",
  "OPENAI_MODEL",
  "DEEPSEEK_BASE_URL",
  "DEEPSEEK_API_KEY",
  "DEEPSEEK_MODEL",
  "OPENCLAW_BASE_URL",
