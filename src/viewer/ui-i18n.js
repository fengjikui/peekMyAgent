export function translateUi(language, key, vars = {}, { resources = UI_I18N, fallbackLanguage = DEFAULT_UI_LANGUAGE } = {}) {
  const dictionary = resources[language] || resources[fallbackLanguage] || {};
  const fallback = resources[fallbackLanguage] || {};
  const template = dictionary[key] ?? fallback[key] ?? key;
  return String(template).replace(/\{(\w+)\}/g, (_, name) => (vars[name] == null ? "" : String(vars[name])));
}
